package com.newsee.oauth.vo;

public class MobilUserInfo {

	private String userName;
	
	private String mobilePhone;
	
	private String departmentID;
	
	private String departmentName;
	
	private String ancestorName;
	
	private String userToken;
	
	private String companyID;
	
	private String appSetting;
	
	private String accountForThirdSystem;

	public String getUserName() {
		return userName;
	}

	public void setUserName(String userName) {
		this.userName = userName;
	}

	public String getMobilePhone() {
		return mobilePhone;
	}

	public void setMobilePhone(String mobilePhone) {
		this.mobilePhone = mobilePhone;
	}

	public String getDepartmentID() {
		return departmentID;
	}

	public void setDepartmentID(String departmentID) {
		this.departmentID = departmentID;
	}

	public String getDepartmentName() {
		return departmentName;
	}

	public void setDepartmentName(String departmentName) {
		this.departmentName = departmentName;
	}

	public String getAncestorName() {
		return ancestorName;
	}

	public void setAncestorName(String ancestorName) {
		this.ancestorName = ancestorName;
	}

	public String getUserToken() {
		return userToken;
	}

	public void setUserToken(String userToken) {
		this.userToken = userToken;
	}

	public String getCompanyID() {
		return companyID;
	}

	public void setCompanyID(String companyID) {
		this.companyID = companyID;
	}

	public String getAppSetting() {
		return appSetting;
	}

	public void setAppSetting(String appSetting) {
		this.appSetting = appSetting;
	}

	public String getAccountForThirdSystem() {
		return accountForThirdSystem;
	}

	public void setAccountForThirdSystem(String accountForThirdSystem) {
		this.accountForThirdSystem = accountForThirdSystem;
	}
	
	
	
}
